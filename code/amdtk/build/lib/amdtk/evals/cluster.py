	precision = true_pos/(true_pos+false_pos)
	recall = true_pos/(true_pos+false_neg)

	f1 = 2*(precision*recall)/(precision+recall)

	return {'accuracy':accuracy, 
			'recall':recall,
			'precision':precision,
			'f1':f1}



